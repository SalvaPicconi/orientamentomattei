    const scrolled = window.pageYOffset;
    const heroCircles = document.querySelectorAll('.hero-circle');
    
    heroCircles.forEach((circle, index) => {
        const speed = (index + 1) * 0.5;
        circle.style.transform = `translateY(${scrolled * speed}px)`;
    });
